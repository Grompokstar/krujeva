//////////////////////////////////////////////////// Sound /////////////////////////////////////////////////
class XHESound extends XHEBaseObject
{
	///////////////////////////////////////////////////////// SERVICVE FUNCTIONS /////////////////////////////////////////////////////////////
	// server initialization
	function XHESound($server,$password="")
	{    
		$this->server = $server;
		$this->password = $password;
		$this->prefix = "Sound";
	}
	//////////////////////////////////////////////////////////// FUNCTIONAL //////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // beep from dynamic
	function beep()
	{
		return $this->call("Sound.Beep");
	}
	// play wav sound by selected path
	function play_sound($filepath)
	{
		if ($this->call("Sound.PlaySound?filepath=".urlencode($filepath))=="true")
			return true;
		else
			return false;
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
};	
?>