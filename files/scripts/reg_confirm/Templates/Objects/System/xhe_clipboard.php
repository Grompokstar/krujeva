//////////////////////////////////////////////////// Clipboard /////////////////////////////////////////////////
class XHEClipboard extends XHEBaseObject
{
	///////////////////////////////////////////////////////// SERVICVE FUNCTIONS /////////////////////////////////////////////////////////////
	// server initialization
	function XHEClipboard($server,$password="")
	{    
		$this->server = $server;
		$this->password = $password;
		$this->prefix = "Clipboard";
	}
	//////////////////////////////////////////////////////////// FUNCTIONAL //////////////////////////////////////////////////

	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	// put text to clipboard
	function put_text($text)
	{
		if ($this->call("Clipboard.PutText?text=".urlencode($text))=="true")      
			return true;
		else
			return false;
	}
	// get text from clipboard
	function get_text()
	{			  
		return $this->call("Clipboard.GetText");
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
};	
?>